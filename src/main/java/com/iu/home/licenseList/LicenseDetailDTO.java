package com.iu.home.licenseList;

public class LicenseDetailDTO {
	private Long licenseNum;
	private Long num;
	private String licenseDate;
	private String licenseSite;
	private String licenseJob;
	
	public Long getLicenseNum() {
		return licenseNum;
	}
	public void setLicenseNum(Long licenseNum) {
		this.licenseNum = licenseNum;
	}
	public Long getNum() {
		return num;
	}
	public void setNum(Long num) {
		this.num = num;
	}
	public String getLicenseDate() {
		return licenseDate;
	}
	public void setLicenseDate(String licenseDate) {
		this.licenseDate = licenseDate;
	}
	public String getLicenseSite() {
		return licenseSite;
	}
	public void setLicenseSite(String licenseSite) {
		this.licenseSite = licenseSite;
	}
	public String getLicenseJob() {
		return licenseJob;
	}
	public void setLicenseJob(String licenseJob) {
		this.licenseJob = licenseJob;
	}
	
	

}
