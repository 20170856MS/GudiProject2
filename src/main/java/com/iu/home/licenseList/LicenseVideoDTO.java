package com.iu.home.licenseList;

public class LicenseVideoDTO {

	private Long licenseVideoNum;
	private Long licenseNum;
	private Long videoNum;
	
	public Long getLicenseVideoNum() {
		return licenseVideoNum;
	}
	public void setLicenseVideoNum(Long licenseVideoNum) {
		this.licenseVideoNum = licenseVideoNum;
	}
	public Long getLicenseNum() {
		return licenseNum;
	}
	public void setLicenseNum(Long licenseNum) {
		this.licenseNum = licenseNum;
	}
	public Long getVideoNum() {
		return videoNum;
	}
	public void setVideoNum(Long videoNum) {
		this.videoNum = videoNum;
	}
	
}
