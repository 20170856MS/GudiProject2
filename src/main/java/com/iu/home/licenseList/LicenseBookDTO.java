package com.iu.home.licenseList;

public class LicenseBookDTO {

	private Long licenseBookNum;
	private Long licenseNum;
	private Long bookNum;
	
	public Long getLicenseBookNum() {
		return licenseBookNum;
	}
	public void setLicenseBookNum(Long licenseBookNum) {
		this.licenseBookNum = licenseBookNum;
	}
	public Long getLicenseNum() {
		return licenseNum;
	}
	public void setLicenseNum(Long licenseNum) {
		this.licenseNum = licenseNum;
	}
	public Long getBookNum() {
		return bookNum;
	}
	public void setBookNum(Long bookNum) {
		this.bookNum = bookNum;
	}
	
	
}
